class Course {
    code;
    title;

    is_required;

    area;

    languages;
    technologies;

    average_gpa;

    constructor(code, title, is_required, area, languages, technolgies, average_gpa) {
        this.code = code;
        this.title = title;
        this.is_required = is_required;
        this.area = area;
        this.languages = languages;
        this.technologies = technolgies;
        this.average_gpa = average_gpa;
    }



}